string = "i am using python"

# imp all element in character start from "0"

# starting character
print(string[0])  # Assuming "0" is starting of the string. this will print "i"
# negative index
print(string[-1])   # this will print last character 'n'

# if we want to some character we can use like "starting index : end index-1 " 

print(string[0:4])   # this will print from 0 upto 3 character

print (string[0:])   # this will print all charcter starting from 0

print(string[2:6])   # this will print all character starting from 2 upto 5

print (string[:6])   # this will print all character from starting upto 5

